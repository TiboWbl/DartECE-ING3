    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Connexion conn = new Connexion();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int index = 0;
        index = jTable1.getSelectedRow();
        String q = model.getValueAt(index, 0).toString();
        Produit p = null;
        try {
            p = conn.RechercherProduit(q);
            controle.getUti().AjouterPanier(p);

            for (int i = 0; i < controle.getUti().getPanier().size(); i++) {
                System.out.println(controle.getUti().getPanier().get(i).getNomProduit());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Jeuxvideo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

