    public void ChargementElectronique() {

        Connexion connn = new Connexion();
        connn.connect();
        try {
            String q = "SELECT `Nom`, `Prix`, `Prix vrac` FROM `produit` WHERE `Categorie` = 'Electronique'";
            pst = connn.conn.prepareStatement(q);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
        }
    }

    public void RechercheNomProduit(String nom) {

        Connexion connn = new Connexion();
        connn.connect();

        try {
            String q = "SELECT `Nom`, `Prix`, `Prix vrac` FROM `produit` WHERE `Categorie` = 'Electronique' AND `Nom`= '" + nom + "'";
            pst = connn.conn.prepareStatement(q);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
        }
    }

